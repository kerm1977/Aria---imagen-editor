export default function EmojiPicker({ visible, x, y, onEmojiSelect, onClose }) {
  const [selectedCategory, setSelectedCategory] = useState('Emociones')

  if (!visible) return null

  return (
    <div 
      className="fixed bg-card/95 backdrop-blur-md border border-border rounded-lg shadow-lg z-[9999] w-80 max-h-96 overflow-hidden"
      style={{ left: x, top: y }}
      onClick={(e) => e.stopPropagation()}
    >
      <div className="flex border-b border-border">
        {Object.keys(emojiCategories).map(category => (
          <button
            key={category}
            onClick={() => setSelectedCategory(category)}
            className={`flex-1 px-2 py-2 text-xs font-medium transition ${
              selectedCategory === category ? 'bg-primary/20 text-primary' : 'hover:bg-primary/10'
            }`}
          >
            {category}
          </button>
        ))}
      </div>
      <div className="p-2 grid grid-cols-8 gap-1 max-h-72 overflow-y-auto">
        {emojiCategories[selectedCategory].map(emoji => (
          <button
            key={emoji}
            onClick={() => onEmojiSelect(emoji)}
            className="text-2xl hover:bg-primary/20 rounded p-1 transition"
          >
            {emoji}
          </button>
        ))}
      </div>
      <button
        onClick={onClose}
        className="w-full px-4 py-2 bg-secondary hover:bg-primary/20 text-sm font-medium transition border-t border-border"
      >
        Cerrar
      </button>
    </div>
  )
}
